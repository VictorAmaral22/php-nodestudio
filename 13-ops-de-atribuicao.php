<?php
/* Operadores de Atribuição */

$a = 10;
$b = 5;

/* Soma */
$a += $b;
echo $a; // 15
echo "<br>";

/* Subtração */
$a -= $b;
echo $a; // 10
echo "<br>";

/* Multiplicação */
$a *= $b;
echo $a; // 50
echo "<br>";

/* Divisão */
$a /= $b;
echo $a; // 10
echo "<br>";

/* Exponencial */
$a **= $b;
echo $a; // 100000
echo "<br>";

/* Módulo */
$a %= $b;
echo $a; // 0
